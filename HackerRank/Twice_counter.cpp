class Solution
{
    public:
        int countWords(string list[], int n)
        {
           map<string, int>check;
           
           for(int i=0;i<n;i++){
               check[list[i]]++;
           }
           
           int cnt=0;
           for(auto it:check){
               if(it.second==2){
                   cnt++;
               }
           }
           
           return cnt;
        }

};


